    public function search(Request $request)
    {
        $from = $request->input('form');
        $to = $request->input('to');

        $query = DB::table('incomes')->select()
                ->where('incomeDate','>=',$from)
                ->where('incomeDate','<=',$to)
                ->get();
        return view('report.reportIncome',compact(['query']));
